class StuCourseDatas {
  final int subOfferedId;
  final String subName;
  final String courseCode;
  final String session;
  final String sessionYear;
  final String engagedBy;
  final int totalClasses;
  final int attendedClasses;
  final List<AttendanceDetail> attendanceDetails;

  StuCourseDatas({
    required this.subOfferedId,
    required this.subName,
    required this.courseCode,
    required this.session,
    required this.sessionYear,
    required this.engagedBy,
    required this.totalClasses,
    required this.attendedClasses,
    required this.attendanceDetails,
  });

  // From JSON
  factory StuCourseDatas.fromJson(Map<String, dynamic> json) {
    return StuCourseDatas(
      subOfferedId: json['sub_offered_id'],
      subName: json['sub_name'],
      courseCode: json['course_code'],
      session: json['session'],
      sessionYear: json['session_year'],
      engagedBy: json['engaged_by'],
      totalClasses: json['total_classes'],
      attendedClasses: json['attended_classes'],
      attendanceDetails: (json['attendance_details'] as List)
          .map((item) => AttendanceDetail.fromJson(item))
          .toList(),
    );
  }

  // To JSON
  Map<String, dynamic> toJson() {
    return {
      'sub_offered_id': subOfferedId,
      'sub_name': subName,
      'course_code': courseCode,
      'session': session,
      'session_year': sessionYear,
      'engaged_by': engagedBy,
      'total_classes': totalClasses,
      'attended_classes': attendedClasses,
      'attendance_details':
          attendanceDetails.map((item) => item.toJson()).toList(),
    };
  }
}

